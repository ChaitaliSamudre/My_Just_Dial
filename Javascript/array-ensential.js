let arr = ['Welcome' ,'to' ,'programming' ,'world'];
//hello
arr.push('Hello');
console.log(`added hello to  ${arr}`);

// pop method
arr.pop();
console.log(`remove the last element from array: ${arr}`);

//unshift method

//add items at the begining

arr.unshift('Hello','Team');
console.log(`add element at the begining: ${arr}`);

//remove the element from the begining
arr.shift();
console.log(`add the element from the begining ${arr}`);




